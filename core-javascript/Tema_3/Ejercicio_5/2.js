/* 2. Write a JavaScript conditional statement to find the sign of the product of three numbers. Display an alert box with the specified sign.
Sample numbers : 3, -7, 2
Output : The sign is -
*/
function sign (x, y, z) {
    if (x > 0 && y > 0 && z > 0) {
        alert("The sign is +");
    } else 
        console.log("The sign is -");
}
sign(3, -7, 2)


