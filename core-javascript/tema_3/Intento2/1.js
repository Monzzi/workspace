let numero = 5;
switch (numero) {
  case 0:
    console.log('0');
    break;
  case 1:
    console.log('1');
    break;
  case 2:
    console.log('2');
    break;
  default:
    console.log('default');
    break;
}

if (numero === 0) {
  console.log('0');
} else if (numero === 1) {
  console.log('1');
} else if (numero === 2) {
  console.log('2');
} else {
  console.log('default');
}
